"""
1.1 Variáveis e Operadores
Escreva um programa que:

Receba dois números do usuário.
Calcule a soma, subtração, multiplicação e divisão dos números.
Imprima os resultados formatados.

"""
numero_1 = float(input('Digite o primeiro numero: '))
numero_2 = float(input('Digite o segundo numero: '))

soma = numero_1 + numero_2
subtracao = numero_1 - numero_2
multiplicacao = numero_1 * numero_2
divisao = numero_1 / numero_2

print(f'Soma: {soma}')
print(f'Subtração: {subtracao}')
print(f'Multiplicação: {multiplicacao}')
print(f'Divisão: {divisao}')

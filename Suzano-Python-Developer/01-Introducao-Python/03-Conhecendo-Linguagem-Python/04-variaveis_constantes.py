"""
Variáveis e Constantes em Python

Em Python, variáveis são criadas ao atribuir um valor a um nome.
Python não possui constantes nativas, mas por convenção, nomes de constantes são escritos em maiúsculas.
"""

# Definição de variáveis
idade = 25
nome = "Alice"
altura = 1.68

# Definição de constante (por convenção)
PI = 3.14159
GRAVIDADE = 9.81

# Exibir os valores
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("Valor de PI:", PI)
print("Aceleração da gravidade:", GRAVIDADE)

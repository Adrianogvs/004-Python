"""
Desfazendo Alterações no Repositório Local

Podemos desfazer alterações de diferentes maneiras:
- `git checkout -- <arquivo>` : Restaura um arquivo modificado
- `git reset HEAD <arquivo>` : Remove arquivos do staging
- `git reset --hard` : Desfaz todas as mudanças não commitadas
"""

# Exemplo de comandos
print("Para descartar alterações locais: git checkout -- <arquivo>")
print("Para remover um arquivo do staging: git reset HEAD <arquivo>")
print("Para resetar completamente: git reset --hard")

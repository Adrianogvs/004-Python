"""
Trabalhando com Branches - Comandos Úteis no Dia a Dia

Alguns comandos úteis ao trabalhar com branches:
- `git branch` : Lista todas as branches
- `git checkout -b <nome>` : Cria e muda para uma nova branch
- `git log --oneline --graph` : Mostra histórico de branches de forma visual
- `git rebase <branch>` : Rebaseia commits de outra branch
"""

# Exemplo de uso
print("Veja todas as branches: git branch")
print("Crie e altere para uma nova branch: git checkout -b <nome>")
print("Visualize o histórico: git log --oneline --graph")

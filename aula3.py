# Vamos entender a função print(), utilizada para exibir algo na tela. Passamos um argumento ou mais para ser exibido na tela.

print(12, 34, sep='-', end='\n') # sep='' refere-se a definição do tipo de separador, neste exemplo usamos o hífen.
print(56, 78, sep='', end='\n') # Já aqui foi tulizado aspas simples que quer dizer que não tem espaço.

# Quebra de linha utiliza \r \n -> CRLF
# \n -> LF

